// ─── NEIGHBORHOOD EXPLORER ───────────────────────────────────────────────────

export type DimensionId =
  | 'affordability'
  | 'income'
  | 'safety'
  | 'transit'
  | 'investment'
  | 'blight'
  | 'parks'     // CAGIS: park acreage per 1,000 residents within 0.75 mi of centroid
  | 'flood'     // FEMA NFHL: whether centroid falls in a Special Flood Hazard Area
  | 'food'      // USDA FARA 2019: % of neighborhood population in a food desert tract
  | 'ej'        // EPA EJScreen 2023: composite air toxics + diesel + Superfund + traffic burden
  | 'schools';

export interface Dimension {
  id: DimensionId;
  labelKey: string;
  descriptionKey: string;
  methodology?: string;  // Plain-English explanation of how the score is computed
  enabled: boolean;
  weight: number;        // 1–5
  available: boolean;    // false = disabled (schools)
  higherIsBetter: boolean;
  incomeSub?: 'higher' | 'lower';
}

export interface NeighborhoodRawMetrics {
  // Affordability
  rentBurdenRate?: number;
  medianGrossRent?: number;
  rentBurdenMOE?: number;
  // Income
  medianHouseholdIncome?: number;
  incomeMOE?: number;
  // Safety
  crimeRatePer1000?: number;
  crimeCount?: number;
  // Transit
  uniqueRouteCount?: number;
  stopCount?: number;
  // Investment
  permitYoYChange?: number;
  recentPermitCount?: number;
  // Blight
  plapPerSqMile?: number;
  firstPassRate?: number;
  // Parks (CAGIS) — acreage within 0.75 mi of neighborhood centroid / pop * 1000
  parkAcresPer1000?: number;
  parkTotalAcres?: number;
  // Flood (FEMA NFHL) — true if centroid is in a Special Flood Hazard Area
  inFloodZone?: boolean;
  // Food Access (USDA FARA 2019) — % of neighborhood population in a LILA food desert tract
  foodDesertPct?: number;     // 0–100; higher = more food desert exposure
  povertyRate?: number;       // weighted average poverty rate across tracts
  medianFamilyIncomeFARA?: number;
  // Population (denominator)
  population?: number;
  // Environmental Justice (EPA EJScreen 2023)
  // National percentile ranks — 0 (least exposed nationally) to 100 (most exposed).
  // ejPollutionIndex is a weighted composite; all individual fields are percentiles.
  ejPollutionIndex?: number;     // composite pollution burden (higher = worse)
  ejCancerPctile?: number;       // air toxics lifetime cancer risk %ile
  ejDieselPctile?: number;       // diesel particulate matter %ile
  ejTrafficPctile?: number;      // traffic proximity %ile
  ejSuperfundPctile?: number;    // Superfund site proximity %ile
  ejWastePctile?: number;        // hazardous waste facility proximity %ile
  ejWastewaterPctile?: number;   // wastewater discharge %ile
  ejPm25Pctile?: number;         // PM 2.5 annual average %ile
}

export interface NeighborhoodScore {
  name: string;
  compositeScore: number;
  dimensionScores: Record<DimensionId, number | null>;
  rawMetrics: NeighborhoodRawMetrics;
  hasInsufficientData: boolean;
}

// ─── LEAD SERVICE LINES ───────────────────────────────────────────────────────

/**
 * Pre-computed lead service line inventory for a single Cincinnati neighborhood.
 * Generated by scripts/build_lead.py from GCWW open data.
 *
 * "Lead or unknown" = lines that may still be exposing residents to lead;
 * these are the civic urgency metric.
 */
export interface NeighborhoodLeadStats {
  /** Neighborhood name (matches SNA list) */
  name: string;
  /** Total service lines in inventory for this neighborhood */
  total: number;
  /** Lines confirmed as lead material */
  lead: number;
  /** Lines with unknown material (assumed risky until confirmed) */
  unknown: number;
  /** Lines confirmed galvanized (corrosion concern, often paired with lead) */
  galvanized: number;
  /** Lines confirmed as copper or other non-lead material */
  copper: number;
  /** Lines already replaced (private-side) */
  replaced: number;
  /** Last time this data was pulled from GCWW */
  asOf?: string;
}

// ─── EJSCREEN ENVIRONMENTAL JUSTICE ──────────────────────────────────────────

/**
 * Pre-computed EPA EJScreen 2023 environmental justice indicators for a
 * single Cincinnati neighborhood. Generated by scripts/build_ejscreen.py.
 *
 * All *Pctile fields are national percentile ranks (0–100):
 * 0 = least exposed nationally, 100 = most exposed nationally.
 * Higher values mean greater pollution burden.
 *
 * ejIndex is the weighted composite of the five key indicator percentiles.
 * Higher ejIndex = greater environmental burden = lower Explorer score
 * (the Explorer dimension uses higherIsBetter: false).
 */
export interface NeighborhoodEJStats {
  name: string;
  ejIndex: number | null;          // composite pollution burden (0–100, higher = worse)
  cancerPctile: number | null;     // air toxics cancer risk national percentile
  dieselPctile: number | null;     // diesel PM national percentile
  trafficPctile: number | null;    // traffic proximity national percentile
  superfundPctile: number | null;  // Superfund site proximity national percentile
  wastePctile: number | null;      // hazardous waste facility proximity national percentile
  wastewaterPctile: number | null; // wastewater discharge national percentile
  pm25Pctile: number | null;       // PM 2.5 annual average national percentile
  tractCount: number;
  pop: number;
  asOf?: string;
}
