  hearing: number;
  /** Vision difficulty count — from B18103 */
  vision: number;
  /** Cognitive difficulty count — from B18104 */
  cognitive: number;
  /** Ambulatory (mobility) difficulty count — from B18105 */
  ambulatory: number;
  /** Self-care difficulty count — from B18106 */
  selfCare: number;
  /** Independent living difficulty count — from B18107 */
  independentLiving: number;
